    private void buscar(String titulo) throws IOException {
        int dialogResult = JOptionPane.NO_OPTION;
        if (titulo.isEmpty()) {
            dialogResult = JOptionPane.showConfirmDialog(null, "Campo de busca vazio, vocÃª deseja listar todos os resultados?", "Aviso", JOptionPane.YES_NO_OPTION);
        }
        if (!titulo.isEmpty() || dialogResult == JOptionPane.YES_OPTION) {
            listarResultados(animeControl.listar(titulo));
        }
    }

    private void listarResultados(List<Anime> lista) {
        pnResultados.removeAll();
        pnResultados.setBorder(javax.swing.BorderFactory.createTitledBorder("Exibindo " + lista.size() + " resultados"));
        pnResultados.setLayout(new GridLayout(0, 5));
        for (Anime anime : lista) {
            System.out.println("Encontrado na busca -> " + anime.getTitulo());
            JLabel labelCapa = new JLabel();
            labelCapa.setToolTipText("Ver todos os detalhes de " + anime.getTitulo());
            labelCapa.setIcon(anime.getCapaImg());
            labelCapa.setCursor(new Cursor(Cursor.HAND_CURSOR));
            //labelCapa.setBorder(javax.swing.BorderFactory.createLineBorder(Color.black, 3, true));
            pnResultados.add(labelCapa);

            labelCapa.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    new JanelaAnime(anime);
                }

            });
        }
        pnResultados.revalidate();
    }

